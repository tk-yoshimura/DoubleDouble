            BitAssert.NeighborBits("1.288022524698077457370610440219717295925", ddouble.LogGamma(0.25), 4);
            BitAssert.NeighborBits("2.032809514312953714814329718624296997597e-1", ddouble.LogGamma(0.75), 4);
