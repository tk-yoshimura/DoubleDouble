                    Console.WriteLine(z);

                    PrecisionAssert.AlmostEqual(x, z, 2e-30d);
                }
            }

            for (double h = Math.ScaleB(1, 101); h <= Math.ScaleB(1, 200); h *= 2) {
                for (double x = h; x < h * 2; x += h / 64) {
