            for (double h = 1d / 1024; h <= Math.ScaleB(1, 400); h *= 2) {
                for (ddouble x = -h; x > -h * 2; x -= h / 64) {
                    ddouble y = ddouble.InverseDigamma(x);
                    ddouble z = ddouble.Digamma(y);

                    Console.WriteLine(x);
                    Console.WriteLine(y);
                    Console.WriteLine(z);

                    PrecisionAssert.AlmostEqual(x, z, 4e-28d);
                }
