        [TestMethod]
        public void ClausenCycleTest() {
            for (ddouble x = -2; x < 4; x += 1d / 32) {
                ddouble y = ddouble.Clausen(x, normalized: true);
                ddouble y_m4 = ddouble.Clausen(x - 4d, normalized: true);
                ddouble y_m2 = ddouble.Clausen(x - 2d, normalized: true);
                ddouble y_p2 = ddouble.Clausen(x + 2d, normalized: true);
                ddouble y_p4 = ddouble.Clausen(x + 4d, normalized: true);

                ddouble y_x2 = ddouble.Clausen(x * 2, normalized: true);
                ddouble y_1m = ddouble.Clausen(1 - x, normalized: true);

                PrecisionAssert.AreEqual(y, y_m4);
                PrecisionAssert.AreEqual(y, y_m2);
                PrecisionAssert.AreEqual(y, y_m2);
                PrecisionAssert.AreEqual(y, y_m4);
                PrecisionAssert.AlmostEqual(y_x2, 2 * (y - y_1m), 2e-31d);
            }
        }

